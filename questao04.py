"""
Fazer um programa que pergunte um número inteiro e apresente o seu antecessor e seu sucessor.
"""

num1 = int(input("Digite um numero de sua escolha para saber seu antecessor e seu sucessor:"))
num2 = 1

soma1 = num1 - num2
soma2 = num1 + num2

print("O antecessor desse número é: ", soma1, "E o sucessor desse número é: ",soma2 )